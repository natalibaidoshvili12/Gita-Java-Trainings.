package november20;

public class Number {

    public int sum(int a, int b) {
        return a + b;
    }

    // sum(int,int)

    public long sum(int a, long b) {
        return a + b;
    }

    public int sum(int a, int b, int c) {
        return a + b + c;
    }

    public int sum(int a, short b) {
        return a + b;
    }

}
